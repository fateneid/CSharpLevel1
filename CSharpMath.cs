using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CSharpLevel1
{
    internal class CSharpMath
    {

        public static void CSharpMathMethods()
        {

            Console.WriteLine("\n_________________________C# Math_________________________");

            Console.WriteLine("Absolute value of -10 is: {0}", Math.Abs(-10));
            Console.WriteLine("Square root of 25 is: {0}", Math.Sqrt(25));
            Console.WriteLine("2 raised to power 3 is: {0}", Math.Pow(2, 3));
            Console.WriteLine("Round of 3.6 is: {0}", Math.Round(3.6));
            Console.WriteLine("Floor of 4.8 is: {0}", Math.Floor(4.8));
            Console.WriteLine("Ceiling of 4.2 is: {0}", Math.Ceiling(4.2));
            Console.WriteLine("Max of 10 and 20 is: {0}", Math.Max(10, 20));
            Console.WriteLine("Min of 10 and 20 is: {0}", Math.Min(10, 20));
            Console.WriteLine("Sin of PI/2 is: {0}", Math.Sin(Math.PI / 2));
            Console.WriteLine("Cos of 0 is: {0}", Math.Cos(0));
            Console.WriteLine("Tan of 0 is: {0}", Math.Tan(0));
            Console.WriteLine("Natural log of 1 is: {0}", Math.Log(1));
            Console.WriteLine("Log10 of 100 is: {0}", Math.Log10(100));
            Console.WriteLine("e^1 is: {0}", Math.Exp(1));
            Console.WriteLine("Sign of -20 is: {0}", Math.Sign(-20));
            Console.WriteLine("Truncate of 5.9 is: {0}", Math.Truncate(5.9));
            Console.WriteLine("Value of PI is: {0}", Math.PI);
            Console.WriteLine("Value of E is: {0}", Math.E);

            Console.WriteLine("_________________________________________________________");

        }



    }
}
